package commandPattern;

public class Electricfan {
	public String switchOn() {
        return "Electricfan is switched-on!";
    }

    public String switchOff(){
        return "Electricfan is switched-off";
    }
    
    public String SpeedUp(){
        return "Electricfan is increasing speed!";
    }

    public String SpeedDown(){
        return "Electricfan is decreasing speed!";
    }
}

